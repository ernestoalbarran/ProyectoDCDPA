		select ID_FIRMANTE1, RFC_PEC,RFC_CONSEJERO1,RFC_CONSEJERO2,RFC_CONSEJERO3,RFC_CONSEJERO4,
	RFC_CONSEJERO5,RFC_CONSEJERO6,SIGLAS,FECHA_OFICIO from FIRMANTE1 where ID_FIRMANTE1=@ID_FIRMANTE1
	
	
