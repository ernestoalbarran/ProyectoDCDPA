	RFC_CONSEJERO5,RFC_CONSEJERO6,SIGLAS,FECHA_OFICIO) values (@RFC_PEC,@RFC_CONSEJERO1,@RFC_CONSEJERO2,@RFC_CONSEJERO3,@RFC_CONSEJERO4,
